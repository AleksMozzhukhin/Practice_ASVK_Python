import sys


# Чтение всего входного текста
input_text = sys.stdin.read()

# Этап 1: Кодируем входной текст в байты UTF-8
# Здесь 'replace' заменяет символы, которые не могут быть закодированы в UTF-8, на '?'
bytes_utf8 = input_text.encode('utf-8')

# Этап 2: Декодируем байты UTF-8 как Latin1, чтобы получить исходные байты CP1251
# 'replace' заменяет символы, которые не могут быть декодированы, на '?'
bytes_cp1251 = bytes_utf8.decode('latin1').encode('cp1251')

# Этап 3: Декодируем байты CP1251 в корректный русский текст
# 'replace' заменяет байты, которые не могут быть декодированы, на '?'
output_text = bytes_cp1251.decode('cp1251')

# Вывод результата без добавления дополнительного перевода строки
print(output_text, end='')

